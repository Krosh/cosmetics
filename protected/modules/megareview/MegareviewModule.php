     * Инициализация модуля, считывание настроек из базы данных и их кэширование
     *
     * @return void
     */
    public function init()
    {
        parent::init();

        $this->setImport(
            [
                'megareview.models.*',
                'megareview.components.*',
            ]
        );
    }

