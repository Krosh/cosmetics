     * Массив правил модуля
     * @return array
     */
    public function getAuthItems()
    {
        return [
            [
                'name' => 'Megareview.MegareviewManager',
                'description' => Yii::t('MegareviewModule.megareview', 'Manage megareview'),
                'type' => AuthItem::TYPE_TASK,
                'items' => [
                    [
                        'type' => AuthItem::TYPE_OPERATION,
                        'name' => 'Megareview.MegareviewBackend.Index',
                        'description' => Yii::t('MegareviewModule.megareview', 'Index')
                    ],
                ]
            ]
        ];
    }
}
